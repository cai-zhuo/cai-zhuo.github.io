#!/usr/bin/env node
/**
 * sync-progress.mjs
 *
 * Reads the canonical RPG World task tracker JSON from the rpg-world repo
 * and writes a self-contained JS snapshot at data/rpg-tasks.js that exposes
 * `window.RPG_TASKS = {...}` for the static portfolio site to consume.
 *
 * Run from the project root:
 *   node scripts/sync-progress.mjs
 */

import { readFileSync, writeFileSync, existsSync, mkdirSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

// --- Config ----------------------------------------------------------------
const SOURCE = "/Users/caizhuo/Workspace/rpg-world/tasks.json";

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const PROJECT_ROOT = resolve(__dirname, "..");
const TARGET = resolve(PROJECT_ROOT, "data", "rpg-tasks.js");

// --- Run -------------------------------------------------------------------
function fail(msg) {
  console.error(`\n✗ sync-progress: ${msg}\n`);
  process.exit(1);
}

if (!existsSync(SOURCE)) {
  fail(
    `source file not found: ${SOURCE}\n  → if rpg-world has moved, edit the SOURCE constant at the top of this script`
  );
}

let raw;
try {
  raw = readFileSync(SOURCE, "utf-8");
} catch (e) {
  fail(`could not read source: ${e.message}`);
}

let parsed;
try {
  parsed = JSON.parse(raw);
} catch (e) {
  fail(`source is not valid JSON: ${e.message}`);
}

if (
  !Array.isArray(parsed.completed) ||
  !Array.isArray(parsed.inProgress) ||
  !Array.isArray(parsed.todo)
) {
  fail(
    "source JSON is missing one of: completed[], inProgress[], todo[]"
  );
}

const targetDir = dirname(TARGET);
if (!existsSync(targetDir)) {
  mkdirSync(targetDir, { recursive: true });
}

const out = `// AUTO-GENERATED by scripts/sync-progress.mjs — do not edit by hand.
// Source: ${SOURCE}
// Synced: ${new Date().toISOString()}
window.RPG_TASKS = ${JSON.stringify(parsed, null, 2)};
`;

writeFileSync(TARGET, out, "utf-8");

console.log(`✓ synced ${parsed.completed.length} completed · ${parsed.inProgress.length} in-progress · ${parsed.todo.length} todo`);
console.log(`  → ${TARGET}`);
